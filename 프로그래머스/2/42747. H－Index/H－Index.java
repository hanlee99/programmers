import java.util.Arrays;
class Solution {
    public int solution(int[] citations) {
        int answer = 0;
        Arrays.sort(citations);
        
        int n=citations.length;
        
        for(int i=0; i<n; i++){
            int h = n-i;
            //System.out.print(h+" ");
            if(citations[i] >= h){
                answer = h;
                break;
            }
            
        }
        return answer;
    }
}